package Students;

public class Students {

	int studentNumber, age;
	String firstInitial, lastName;
	double average;

	public void setStudentNumber(int s) {

		studentNumber = s;

	}

	public void setAge(int a) {

		age = a;

	}

	public void setFirstInitial(String f) {

		firstInitial = f;

	}

	public void setLastName(String l) {

		lastName = l;

	}

	public void setAverage(double av) {

		average = av;
	}

	
	
	public int getAge() {

		return age;

	}

	public String getFirstInitial() {

		return firstInitial;

	}

	public String getLastName() {

		return lastName;

	}

	public double getAverage() {

		return average;

	}

	public int getStudentNumber() {

		return studentNumber;

	}

}
